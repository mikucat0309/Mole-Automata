mole_proto = Proto("Mole", "Mole Game Protocol")

f_length    = ProtoField.uint32("mole.length"  , "Length"  , base.DEC)
f_nonce     = ProtoField.uint8( "mole.nonce"   , "Nonce"   , base.DEC)
f_command   = ProtoField.uint32("mole.command" , "Command" , base.DEC)
f_user      = ProtoField.uint32("mole.user"    , "User"    , base.DEC)
f_error     = ProtoField.int32( "mole.error"   , "Error"   , base.DEC)
f_plain     = ProtoField.bytes( "mole.plain"   , "Plain")
mole_proto.fields = { f_length, f_nonce, f_command, f_user, f_error, f_plain }

function decrypt(cipher)
    local plen = #cipher - 1
    local plain = ""
    local ki = 1
    for i=1, plen do
        local ki2 = ki
        if ki2 == 22 then ki2 = 1 end
        local a = bit32.rshift(cipher:byte(i), 5)
        local b = bit32.lshift(cipher:byte(i+1), 3)
        local c = bit32.bor(a, b) % 256
        local d = KEY:byte(ki2)
        plain = plain .. string.char(bit32.bxor(c, d))
        ki = ki + 1
        if ki == 23 then ki = 1 end
    end
    return plain
end

function get_mole_pdu_length(tvb, pinfo, offset)
    local length  = tvb(offset, 4):uint()
    return length
end

function dissect_mole_pdu(tvb, pinfo, tree)
    local length  = tvb( 0, 4):uint()
    local nonce   = tvb( 4, 1):uint()
    local command = tvb( 5, 4):uint()
    local user    = tvb( 9, 4):uint()
    local errorr  = tvb(13, 4):int()
    local command_name = MAPPING[command]
    if command_name == nil then command_name = "Unknown" end
    pinfo.cols.protocol = mole_proto.name
    pinfo.cols.info = command_name
    local subtree = tree:add(mole_proto, tvb(), "Mole Game Protocol, Len: "..length..", Cmd: "..command..", User: "..user..", Error: "..errorr)
    subtree:add(f_length  , tvb( 0, 4))
    subtree:add(f_nonce   , tvb( 4, 1))
    subtree:add(f_command , tvb( 5, 4), command, "Command: "..command.."\t("..command_name..")")
    subtree:add(f_user    , tvb( 9, 4))
    subtree:add(f_error   , tvb(13, 4))
    local cipher_length = tvb(0, 4):uint() - 17
    if cipher_length > 1 then
        local plain = decrypt(tvb(17, cipher_length):raw())
        subtree:add(f_plain, tvb(17, cipher_length), plain)
    end
    return tvb:len()
end

function mole_proto.dissector(tvb, pinfo, tree)
    dissect_tcp_pdus(tvb, tree, MOLE_PDU_HEADER_LENGTH, get_mole_pdu_length, dissect_mole_pdu)
end

