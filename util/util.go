package util

func Int(u int64, err error) int {
	return int(u)
}

func Int16(u int64, err error) int16 {
	return int16(u)
}

func Int32(u int64, err error) int32 {
	return int32(u)
}

func UInt16(u uint64, err error) uint16 {
	return uint16(u)
}

func UInt32(u uint64, err error) uint32 {
	return uint32(u)
}
